r = 1.0
cons1 = {}

for cons in stars:
  cons1 [cons] = []
  for ra,dec,mag in stars[cons]:
    lon = ra
    lat = dec
    x = r * cos (lat) * cos (lon)
    y = r * cos (lat) * sin (lon)
    z = r * sin (lat)
    cons1 [cons].append ((round(x,3),round(y,3),round(z,3),mag))

print (cons1)

